package library.entities;

public class Book {

	private int id;
	private String author;
	private String title;
	private String callNumber;
	
	public int getID()
	{
		return id;
	}
	
	public String getAuthor()
	{
		return author;
	}
	
	public String getTitle()
	{
		return title;
	}
	
	public String getCallNumber()
	{
		return callNumber;
	}
}
